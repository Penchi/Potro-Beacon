/**
 * A login screen that offers login via email/password.
 */
public class LoginActivity extends Activity
{
    private EditText txtId;
    private SharedPreferences sp;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_login);

        txtId = (EditText) findViewById(R.id.txtID);
        sp = getSharedPreferences("PotroBeacon", 0);
        txtId.setText(getSharedPreferences("PotroBeacon", 0).getString("id", "").toString());
    }


    public void Login(View v) {
        if (txtId.getText().toString().equalsIgnoreCase(sp.getString("id", "nada").toString())) {
            Intent intent;

            //Está registrado
            if (sp.getBoolean("maestro", false)) {
                //Es maestro
                intent = new Intent(this, MaestroActivity.class);
            } else {
                //No es maestro
                intent = new Intent(this, AlumnoActivity.class);
            }
            startActivity(intent);
        }
    }


    public void RegistrarInformacion(View v)
    {
        Intent intent = new Intent(this, RegistrarUsuario.class);
        startActivity(intent);
    }
}

